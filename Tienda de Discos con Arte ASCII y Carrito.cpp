typedef struct
{
    string Foto;
    string Banda;
    string Album;
    float Precio;
} Disco;

void MostrarCatalogo(const vector<Disco>&Catalogo){
    cout<<"[-] Catalogo de discos [-] \n";
    for (size_t i = 0; i < Catalogo.size(); i++)
    {
        cout<<Catalogo[i].Foto<<endl<<". "<<Catalogo[i].Banda<<" - "<<Catalogo[i].Album<<" - $"<<Catalogo[i].Precio<<"\n";
    }
}
void AgregarAlCarro(const Disco& disco, vector<Disco>& carrito){
    
    carrito.push_back(disco);
    cout<<"[!] Disco agregado al carrito: "<<disco.Banda<<" - "<<disco.Album<<" [!]\n";
}
void MostrarCarrito(const vector<Disco>& carrito){
    if (carrito.empty())
    {
        cout<<"[?] El carrito esta vacio [?]\n";
    }else {
        cout<<"[!] Carrito de compras [!]\n";
        float Total = 0;
        for (size_t i = 0; i < carrito.size(); i++)
        {
            cout<<i+1<<". "<<carrito[i].Banda<<" - "<<carrito[i].Album<<" - $"<<carrito[i].Precio<<"\n";
            Total += carrito[i].Precio;
            cout<<"Tu total es de $"<<Total<<"\n";
        }
        
    }
    
}



